// Boolean variables representing true or false
let isUserLoggedIn = true;
let hasPermission = false;

// Using Boolean values in conditional statements
if (isUserLoggedIn) {
  console.log("User is logged in");
} else {
  console.log("User is not logged in");
}

// Another conditional example
if (hasPermission) {
  console.log("User has permission to access the page.");
} else {
  console.log("User does not have permission to access the page.");
}

// Boolean values can also result from comparisons
let number1 = 10;
let number2 = 5;

let isGreater = number1 > number2;
console.log("Is number1 greater than number2 ?", isGreater);
